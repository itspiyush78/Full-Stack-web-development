function add(x, y){
    return x+y;
}

let addTwoNumbers = function (x, y){ // anonymous function  expression
    return x+y;
}

let addTwoNumbersAgain = function eval(x, y){ // named function expression
    return x+y;
}

console.log(add(10,2));
console.log(addTwoNumbers(9, 8));
console.log(addTwoNumbersAgain(1, 2));